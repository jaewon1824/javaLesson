public class A06PrefixPostfixTest {
    


public static void main(String[] args) {
    
    int start = 1000;
    System.out.println(start--);
    System.out.println(start--);
    System.out.println(start--);
    System.out.println(start--);


    start = 1000;
    System.out.println(--start);
    System.out.println(--start);
    System.out.println(--start);
    System.out.println(--start);
}}
